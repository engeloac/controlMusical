    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        boolean isValidLong = true;
        boolean isEmployeeNotExist;
        boolean isPopActive = false;
        Long id = null;
        com.usr.services.Find find = com.usr.services.Find.getInstance();
        //com.usr.services.Create create = com.usr.services.Create.getInstance();
        EntityManagerFactory entityManagerFactory = Persistence.createEntityManagerFactory("controlMusicalPU");
        EntityManager entityManager = entityManagerFactory.createEntityManager();
        entityManager.getTransaction().begin();
        Artist employee1 = new Artist();
        
        try {
            id = Long.parseLong(jTextField3.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite un ID valido");
            isValidLong = false;
            isPopActive = true;
        }
        
        if (isValidLong) employee1 = find.findArtist(jTextField1.getText());
        else employee1.setName("notUser");
        
        Artist artist = new Artist();
        
        isEmployeeNotExist = (employee1 == null);
        if (isEmployeeNotExist) {
            artist.setName(jTextField1.getText());
            artist.setLastname(jTextField2.getText());
            artist.setAge(Integer.parseInt(jTextField3.getText()));
            artist.setNumberdiscs(0);
            entityManager.persist(artist);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            JOptionPane.showMessageDialog(null, "Registrado Correctamente");
            entityManager.getTransaction().commit();
            entityManager.close();
            entityManagerFactory.close();
        }
        else {
            if (!isPopActive) JOptionPane.showMessageDialog(null, "Artista, ya est√° registrado");
            jTextField1.setText("");
        }
    }//GEN-LAST:event_jButton1MouseClicked

